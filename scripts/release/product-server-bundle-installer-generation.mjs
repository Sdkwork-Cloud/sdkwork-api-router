function toPortablePath(value) {
  return String(value ?? '').replaceAll('\\', '/');
}

function toWindowsRelativePath(value) {
  return String(value ?? '').replaceAll('/', '\\');
}

function normalizeBundleInstallerSpec(runtimeLayout = {}) {
  return {
    shell: String(runtimeLayout?.bundleInstallers?.shell ?? 'install.sh').trim() || 'install.sh',
    powershell: String(runtimeLayout?.bundleInstallers?.powershell ?? 'install.ps1').trim() || 'install.ps1',
  };
}

function resolveServiceBinaryName(runtimeLayout = {}) {
  return String(runtimeLayout?.serviceBinaryName ?? 'router-product-service').trim() || 'router-product-service';
}

function resolveServiceBinaryFileName(runtimeLayout = {}, platformId = '') {
  const serviceBinaryName = resolveServiceBinaryName(runtimeLayout);
  return String(platformId) === 'windows' ? `${serviceBinaryName}.exe` : serviceBinaryName;
}

function renderMaterializedLayoutPathExpression(materializedPath, replacements = []) {
  return replacements.reduce(
    (renderedPath, { placeholder, expression }) =>
      renderedPath.replaceAll(placeholder, expression),
    String(materializedPath ?? ''),
  );
}

function createInstalledRuntimeLayoutExpressions({
  productRootPlaceholder,
  releaseVersionPlaceholder,
  productRootExpression,
  releaseVersionExpression,
  pathApi,
  platform,
} = {}) {
  const controlLayout = materializeInstalledRuntimeControlLayout({
    productRoot: productRootPlaceholder,
    platform,
    pathApi,
  });
  const releasePayloadLayout = materializeInstalledRuntimeReleasePayloadLayout({
    productRoot: productRootPlaceholder,
    platform,
    releaseVersion: releaseVersionPlaceholder,
    pathApi,
  });
  const replacements = [
    { placeholder: productRootPlaceholder, expression: productRootExpression },
    { placeholder: releaseVersionPlaceholder, expression: releaseVersionExpression },
  ];

  return {
    controlRoot: renderMaterializedLayoutPathExpression(controlLayout.controlRoot, replacements),
    controlBin: renderMaterializedLayoutPathExpression(controlLayout.binDir, replacements),
    controlBinLib: renderMaterializedLayoutPathExpression(controlLayout.binLibDir, replacements),
    systemdDir: renderMaterializedLayoutPathExpression(controlLayout.serviceSystemdDir, replacements),
    launchdDir: renderMaterializedLayoutPathExpression(controlLayout.serviceLaunchdDir, replacements),
    windowsTaskDir: renderMaterializedLayoutPathExpression(controlLayout.serviceWindowsTaskDir, replacements),
    windowsServiceDir: renderMaterializedLayoutPathExpression(controlLayout.serviceWindowsServiceDir, replacements),
    currentManifest: renderMaterializedLayoutPathExpression(controlLayout.releaseManifestFile, replacements),
    releasesRoot: renderMaterializedLayoutPathExpression(releasePayloadLayout.releasesRoot, replacements),
    releaseRoot: renderMaterializedLayoutPathExpression(releasePayloadLayout.releaseRoot, replacements),
    releaseBinDir: renderMaterializedLayoutPathExpression(releasePayloadLayout.releaseBinDir, replacements),
    staticDataDir: renderMaterializedLayoutPathExpression(releasePayloadLayout.staticDataDir, replacements),
    releaseDeployDir: renderMaterializedLayoutPathExpression(releasePayloadLayout.releaseDeployDir, replacements),
    adminSiteDistDir: renderMaterializedLayoutPathExpression(releasePayloadLayout.adminSiteDistDir, replacements),
    portalSiteDistDir: renderMaterializedLayoutPathExpression(releasePayloadLayout.portalSiteDistDir, replacements),
    releasePayloadManifestFile: renderMaterializedLayoutPathExpression(
      releasePayloadLayout.releasePayloadManifestFile,
      replacements,
    ),
    releasePayloadReadmeFile: renderMaterializedLayoutPathExpression(
      releasePayloadLayout.releasePayloadReadmeFile,
      replacements,
    ),
  };
}

function renderJsonArrayLines(values = [], {
  indent = '    ',
} = {}) {
  return values
    .map((value) => `${indent}${JSON.stringify(String(value))}`)
    .join(',\n');
}

function buildShellHereDoc(tag, targetExpression, contents, {
  expandVariables = false,
} = {}) {
  const delimiter = expandVariables ? tag : `'${tag}'`;
  return [
    `  write_text ${targetExpression} <<${delimiter}`,
    contents,
    `  ${tag}`,
  ].join('\n');
}

function buildPowerShellHereString(targetExpression, contents, {
  ifMissing = false,
} = {}) {
  return [
    `    $content = @'`,
    contents,
    `'@`,
    ifMissing
      ? `    Write-TextFileIfMissing -Path ${targetExpression} -Contents $content`
      : `    Write-TextFile -Path ${targetExpression} -Contents $content`,
  ].join('\n');
}

