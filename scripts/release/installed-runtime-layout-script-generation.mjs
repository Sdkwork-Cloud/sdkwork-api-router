import {
  findInstalledRuntimeLayoutSpec,
} from './installed-runtime-layout-catalog.mjs';

function splitRelativePath(relativePath) {
  return String(relativePath ?? '')
    .split('/')
    .filter(Boolean);
}

function joinPosixPath(basePath, segments = []) {
  const filteredSegments = Array.isArray(segments)
    ? segments.filter((segment) => String(segment ?? '').length > 0)
    : [];
  if (filteredSegments.length === 0) {
    return String(basePath ?? '');
  }

  const normalizedBasePath = String(basePath ?? '').replace(/\/+$/u, '');
  if (!normalizedBasePath) {
    return filteredSegments.join('/');
  }

  return `${normalizedBasePath}/${filteredSegments.join('/')}`;
}

function joinWindowsPath(basePath, segments = []) {
  const filteredSegments = Array.isArray(segments)
    ? segments.filter((segment) => String(segment ?? '').length > 0)
    : [];
  if (filteredSegments.length === 0) {
    return String(basePath ?? '');
  }

  const normalizedBasePath = String(basePath ?? '').replace(/[\\/]+$/u, '');
  if (!normalizedBasePath) {
    return filteredSegments.join('\\');
  }

  return `${normalizedBasePath}\\${filteredSegments.join('\\')}`;
}

function normalizeGeneratedInstalledRuntimeLayoutContract() {
  const controlSpec = findInstalledRuntimeLayoutSpec('control-root');
  const portableSpec = findInstalledRuntimeLayoutSpec('portable-mutable-runtime');
  const windowsSystemSpec = findInstalledRuntimeLayoutSpec('system-runtime-windows');
  const macosSystemSpec = findInstalledRuntimeLayoutSpec('system-runtime-macos');
  const linuxSystemSpec = findInstalledRuntimeLayoutSpec('system-runtime-linux');

  const controlRootDirName = splitRelativePath(controlSpec.rootDir).at(-1);

  return {
    controlRootDirName,
    defaultInstallHomePosix: joinPosixPath('', [
      ...portableSpec.productRootDefaultSegments,
      controlRootDirName,
    ]),
    defaultInstallHomeWindows: joinWindowsPath('', [
      ...portableSpec.productRootDefaultSegments,
      controlRootDirName,
    ]),
    releaseManifestFileName: splitRelativePath(controlSpec.releaseManifestFile).at(-1),
    portableConfigRootDirName: splitRelativePath(portableSpec.configRoot).at(-1),
    portableDataRootDirName: splitRelativePath(portableSpec.dataRoot).at(-1),
    portableLogRootDirName: splitRelativePath(portableSpec.logRoot).at(-1),
    portableRunRootDirName: splitRelativePath(portableSpec.runRoot).at(-1),
    shellDefaults: {
      macos: {
        productRoot: joinPosixPath(
          macosSystemSpec.productRootBaseDefault,
          macosSystemSpec.productRootSegments,
        ),
        configRoot: joinPosixPath(
          macosSystemSpec.productRootBaseDefault ? macosSystemSpec.configRootBaseDefault : '',
          macosSystemSpec.configRootSegments,
        ),
        dataRoot: joinPosixPath(macosSystemSpec.dataRootBaseDefault, macosSystemSpec.dataRootSegments),
        logRoot: joinPosixPath(macosSystemSpec.logRootBaseDefault, macosSystemSpec.logRootSegments),
        runRoot: joinPosixPath(macosSystemSpec.runRootBaseDefault, macosSystemSpec.runRootSegments),
      },
      linux: {
        productRoot: joinPosixPath(
          linuxSystemSpec.productRootBaseDefault,
          linuxSystemSpec.productRootSegments,
        ),
        configRoot: joinPosixPath(linuxSystemSpec.configRootBaseDefault, linuxSystemSpec.configRootSegments),
        dataRoot: joinPosixPath(linuxSystemSpec.dataRootBaseDefault, linuxSystemSpec.dataRootSegments),
        logRoot: joinPosixPath(linuxSystemSpec.logRootBaseDefault, linuxSystemSpec.logRootSegments),
        runRoot: joinPosixPath(linuxSystemSpec.runRootBaseDefault, linuxSystemSpec.runRootSegments),
      },
    },
    powerShellDefaults: {
      windows: {
        productRoot: joinWindowsPath(windowsSystemSpec.productRootBaseDefault, windowsSystemSpec.productRootSegments),
        configRoot: joinWindowsPath(windowsSystemSpec.configRootBaseDefault, windowsSystemSpec.configRootSegments),
        dataRoot: joinWindowsPath(windowsSystemSpec.dataRootBaseDefault, windowsSystemSpec.dataRootSegments),
        logRoot: joinWindowsPath(windowsSystemSpec.logRootBaseDefault, windowsSystemSpec.logRootSegments),
        runRoot: joinWindowsPath(windowsSystemSpec.runRootBaseDefault, windowsSystemSpec.runRootSegments),
      },
      macos: {
        productRoot: joinPosixPath(macosSystemSpec.productRootBaseDefault, macosSystemSpec.productRootSegments),
        configRoot: joinPosixPath(macosSystemSpec.configRootBaseDefault, macosSystemSpec.configRootSegments),
        dataRoot: joinPosixPath(macosSystemSpec.dataRootBaseDefault, macosSystemSpec.dataRootSegments),
        logRoot: joinPosixPath(macosSystemSpec.logRootBaseDefault, macosSystemSpec.logRootSegments),
        runRoot: joinPosixPath(macosSystemSpec.runRootBaseDefault, macosSystemSpec.runRootSegments),
      },
      linux: {
        productRoot: joinPosixPath(linuxSystemSpec.productRootBaseDefault, linuxSystemSpec.productRootSegments),
        configRoot: joinPosixPath(linuxSystemSpec.configRootBaseDefault, linuxSystemSpec.configRootSegments),
        dataRoot: joinPosixPath(linuxSystemSpec.dataRootBaseDefault, linuxSystemSpec.dataRootSegments),
        logRoot: joinPosixPath(linuxSystemSpec.logRootBaseDefault, linuxSystemSpec.logRootSegments),
        runRoot: joinPosixPath(linuxSystemSpec.runRootBaseDefault, linuxSystemSpec.runRootSegments),
      },
    },
  };
}

